24 SELECT  
      YEAR(ORDERDATE) AS ORDERYEAR, 
      MONTH(ORDERDATE) AS ORDERMONTH,
      SUM(TOTALAMOUNT) AS TOTALSALES, 
	  COUNT(DISTINCT PRODUCTID) AS UNIQUEPRODUCTS
