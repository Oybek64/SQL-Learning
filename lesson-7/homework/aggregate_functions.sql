GROUP BY YEAR(ORDERDATE), MONTH(ORDERDATE)
HAVING COUNT(DISTINCT PRODUCTID) >= 2;

25 SELECT  
   YEAR(ORDERDATE) AS ORDERYEAR,
   MIN(QUANTITY) AS MIN_QTY,
   MAX(QUANTITY) AS MAX_QTY
   FROM ORDERS
